-- 8
CREATE OR REPLACE TRIGGER TRG_CHECK_HIRE_DATE
BEFORE INSERT ON EMPLOYEES
FOR EACH ROW
BEGIN
    IF :NEW.HIRE_DATE < TO_DATE('1990-01-01', 'YYYY-MM-DD') THEN
        RAISE_APPLICATION_ERROR(-20502, 'Hire date must be on or after January 1, 1990.');
    END IF;
END;

-- 9
CREATE OR REPLACE TRIGGER TRG_SALARY_HISTORY
AFTER UPDATE OF SALARY ON EMPLOYEES
FOR EACH ROW
BEGIN
    IF :OLD.SALARY != :NEW.SALARY THEN
        INSERT INTO SALARY_HISTORY(EMPLOYEE_ID, OLD_SALARY, NEW_SALARY, UPDATE_DATE)
        VALUES(:NEW.EMPLOYEE_ID, :OLD.SALARY, :NEW.SALARY, SYSDATE);
    END IF;
END;

-- 10
CREATE OR REPLACE TRIGGER TRG_SET_MANAGER_LEVEL
BEFORE INSERT ON EMPLOYEES
FOR EACH ROW
DECLARE
    MGR_JOB_ID VARCHAR2(20);
BEGIN
    SELECT JOB_ID INTO MGR_JOB_ID FROM EMPLOYEES WHERE EMPLOYEE_ID = :NEW.MANAGER_ID;
    :NEW.MANAGER_LEVEL := MGR_JOB_ID;
EXCEPTION
    WHEN NO_DATA_FOUND THEN
        :NEW.MANAGER_LEVEL := NULL;
END;

-- 11
CREATE OR REPLACE TRIGGER TRG_DEPT_BUDGET_MAX
BEFORE INSERT OR UPDATE OF SALARY ON EMPLOYEES
FOR EACH ROW
DECLARE
    CURRENT_TOTAL NUMBER;
BEGIN
    SELECT SUM(SALARY) INTO CURRENT_TOTAL FROM EMPLOYEES 
    WHERE DEPARTMENT_ID = :NEW.DEPARTMENT_ID AND EMPLOYEE_ID != NVL(:NEW.EMPLOYEE_ID, -1);
    IF (CURRENT_TOTAL + :NEW.SALARY) > 200000 THEN
        RAISE_APPLICATION_ERROR(-20503, 'Total department salary cannot be greatre than 200,000.');
    END IF;
END;

-- 12
CREATE OR REPLACE TRIGGER TRG_PROMOTION_BONUS
BEFORE UPDATE OF JOB_ID ON EMPLOYEES
FOR EACH ROW
DECLARE
    OLD_RANK NUMBER;
    NEW_RANK NUMBER;
BEGIN
    SELECT RANK INTO OLD_RANK FROM JOB_HIERARCHY WHERE JOB_ID = :OLD.JOB_ID;
    SELECT RANK INTO NEW_RANK FROM JOB_HIERARCHY WHERE JOB_ID = :NEW.JOB_ID;
    IF NEW_RANK > OLD_RANK THEN
        :NEW.SALARY := :OLD.SALARY * 1.05;
    END IF;
END;

