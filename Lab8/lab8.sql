-- 1
CREATE OR REPLACE TRIGGER TRG_EMPLOYEE_AUDIT 
AFTER INSERT ON EMPLOYEES
FOR EACH ROW
BEGIN
    INSERT INTO EMPLOYEE_AUDIT(EMPLOYEE_ID, FIRST_NAME, HIRE_DATE, INSERTION_TIMESTAMP)
    VALUES (:NEW.EMPLOYEE_ID, :NEW.fIRST_NAME, :NEW.HIRE_DATE, SYSTIMESTAMP);
END;

-- 2
CREATE OR REPLACE TRIGGER TRG_STOP_HIGH_SALARY
BEFORE UPDATE OF SALARY ON EMPLOYEES
FOR EACH ROW
BEGIN
    IF :NEW.SALARY > 50000 THEN
        RAISE_APPLICATION_ERROR(-20500, 'Salary cannot be larger than 50000.');
    END IF;
END;

