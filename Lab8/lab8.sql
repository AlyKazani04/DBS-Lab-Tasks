CREATE OR REPLACE TRIGGER TRG_DEPT_LOG_NAMES
AFTER UPDATE OF DEPARTMENT_NAME ON DEPARTMENTS
FOR EACH ROW
BEGIN 
    INSERT INTO DEPT_LOG(OLD_NAME, NEW_NAME, CHANGE_DATE)
    VALUES(:OLD.DEPARTMENT_NAME, :NEW.DEPARTMENT_NAME, SYSDATE);
END;

