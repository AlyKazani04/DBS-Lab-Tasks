CREATE VIEW ABOVE_AVG_DEPT AS
    SELECT * FROM EMPLOYEES E
    WHERE SALARY > (
        SELECT AVG(SALARY) FROM EMPLOYEES 
        WHERE DEPARTMENT_ID = E.DEPARTMENT_ID
    )
;

-- 31
CREATE VIEW MANAGER_DEPT_CHECK AS
    SELECT E.LAST_NAME  AS EMP, 
        E.DEPARTMENT_ID AS EMP_DEPT, 
        M.LAST_NAME     AS MGR, 
        M.DEPARTMENT_ID AS MGR_DEPT
    FROM EMPLOYEES E JOIN EMPLOYEES M 
        ON E.MANAGER_ID = M.EMPLOYEE_ID
;

SELECT * FROM MANAGER_DEPT_CHECK 
    WHERE EMP_DEPT != MGR_DEPT;
    
-- 32
CREATE VIEW EMP_CITY_VIEW AS
    SELECT E.EMPLOYEE_ID, L.CITY FROM EMPLOYEES E 
    JOIN DEPARTMENTS D 
        ON E.DEPARTMENT_ID = D.DEPARTMENT_ID
    JOIN LOCATIONS L 
        ON D.LOCATION_ID = L.LOCATION_ID
;

SELECT * FROM EMP_CITY_VIEW 
WHERE CITY IN (
    SELECT CITY FROM EMP_CITY_VIEW 
    GROUP BY CITY 
    HAVING COUNT(*) > 5
);

-- 33
CREATE VIEW SAL_VS_AVG AS
    SELECT LAST_NAME, SALARY, (
        SELECT AVG(SALARY) FROM EMPLOYEES E2 
            WHERE E2.DEPARTMENT_ID = E1.DEPARTMENT_ID
        ) AS DEPT_AVG
    FROM EMPLOYEES E1
;

SELECT * FROM SAL_VS_AVG 
    WHERE SALARY > DEPT_AVG;
    
-- 34
CREATE VIEW EMP_DEPT_HISTORY AS
    SELECT EMPLOYEE_ID, DEPARTMENT_ID FROM EMPLOYEES
    UNION
    SELECT EMPLOYEE_ID, DEPARTMENT_ID FROM JOB_HISTORY
;

SELECT EMPLOYEE_ID FROM EMP_DEPT_HISTORY 
    GROUP BY EMPLOYEE_ID 
    HAVING COUNT(DEPARTMENT_ID) > 1;
    
-- 35
CREATE VIEW DEPT_COUNTS AS
    SELECT DEPARTMENT_ID, COUNT(*) AS EMP_COUNT 
    FROM EMPLOYEES 
        GROUP BY DEPARTMENT_ID
;

SELECT * FROM DEPT_COUNTS 
    WHERE EMP_COUNT > (
        SELECT AVG(COUNT(*)) FROM EMPLOYEES 
        GROUP BY DEPARTMENT_ID
    )
;

-- 36
CREATE VIEW SAL_COMPARE AS
    SELECT E.LAST_NAME AS EMP, E.SALARY AS E_SAL, M.SALARY AS M_SAL
    FROM EMPLOYEES E 
    JOIN EMPLOYEES M 
        ON E.MANAGER_ID = M.EMPLOYEE_ID
;

SELECT * FROM SAL_COMPARE 
    WHERE E_SAL > M_SAL;
    
-- 37
CREATE VIEW EMP_MGR_LOC AS
    SELECT E.LAST_NAME, L.CITY AS EMP_CITY, LM.CITY AS MGR_CITY
    FROM EMPLOYEES E 
    JOIN DEPARTMENTS D 
        ON E.DEPARTMENT_ID = D.DEPARTMENT_ID
    JOIN LOCATIONS L 
        ON D.LOCATION_ID = L.LOCATION_ID
    JOIN EMPLOYEES M 
        ON E.MANAGER_ID = M.EMPLOYEE_ID
    JOIN DEPARTMENTS DM 
        ON M.DEPARTMENT_ID = DM.DEPARTMENT_ID
    JOIN LOCATIONS LM 
        ON DM.LOCATION_ID = LM.LOCATION_ID
;

SELECT * FROM EMP_MGR_LOC WHERE EMP_CITY = MGR_CITY;

-- 38
CREATE VIEW ROLE_CHECK AS
    SELECT E.EMPLOYEE_ID, E.JOB_ID AS CURRENT_JOB, J.JOB_ID AS PAST_JOB
    FROM EMPLOYEES E 
    JOIN JOB_HISTORY J
        ON E.EMPLOYEE_ID = J.EMPLOYEE_ID
;

SELECT DISTINCT EMPLOYEE_ID FROM ROLE_CHECK 
    WHERE CURRENT_JOB = PAST_JOB;
    
