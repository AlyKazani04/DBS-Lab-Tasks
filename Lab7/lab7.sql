CREATE VIEW HIRE_DATE_COMPARE AS
    SELECT E.LAST_NAME, E.HIRE_DATE AS EMP_HIRE, M.HIRE_DATE AS MGR_HIRE
    FROM EMPLOYEES E 
    JOIN EMPLOYEES M 
        ON E.MANAGER_ID = M.EMPLOYEE_ID
;

SELECT * FROM HIRE_DATE_COMPARE 
    WHERE EMP_HIRE < MGR_HIRE;
    
