-- 1
SELECT E.FIRST_NAME, E.LAST_NAME, E.MANAGER_ID, M.FIRST_NAME AS MANAGER_FIRST_NAME, M.LAST_NAME AS MANAGER_LAST_NAME 
FROM EMPLOYEES E 
LEFT JOIN EMPLOYEES M
ON M.EMPLOYEE_ID = E.MANAGER_ID;

-- 2
SELECT E.EMPLOYEE_ID, E.FIRST_NAME, E.SALARY, E.MANAGER_ID, M.FIRST_NAME AS MANAGER_FIRST_NAME, M.SALARY AS MANAGER_SALARY
FROM EMPLOYEES E 
JOIN EMPLOYEES M
ON M.EMPLOYEE_ID = E.MANAGER_ID
WHERE E.SALARY > M.SALARY;

-- 3
SELECT D.DEPARTMENT_ID, D.DEPARTMENT_NAME FROM DEPARTMENTS D
LEFT JOIN EMPLOYEES E
ON E.DEPARTMENT_ID = D.DEPARTMENT_ID
WHERE E.EMPLOYEE_ID IS NULL;

-- 4
SELECT E.EMPLOYEE_ID, E.HIRE_DATE, M.EMPLOYEE_ID AS MANAGER_ID, M.HIRE_DATE AS MANAAGER_HIRE FROM EMPLOYEES E
JOIN EMPLOYEES M
ON E.MANAGER_ID = M.EMPLOYEE_ID
WHERE E.HIRE_DATE > M.HIRE_DATE;

-- 5
SELECT E1.EMPLOYEE_ID, E1.FIRST_NAME, E1.LAST_NAME, E2.EMPLOYEE_ID, E2.FIRST_NAME, E2.LAST_NAME 
FROM EMPLOYEES E1 JOIN EMPLOYEES E2
ON E1.DEPARTMENT_ID = E2.DEPARTMENT_ID AND E1.EMPLOYEE_ID != E2.EMPLOYEE_ID;

-- 6
SELECT E.FIRST_NAME, E.LAST_NAME, D.DEPARTMENT_NAME, L.CITY
FROM EMPLOYEES E
JOIN DEPARTMENTS D
    ON E.DEPARTMENT_ID = D.DEPARTMENT_ID
JOIN LOCATIONS L
    ON D.LOCATION_ID = L.LOCATION_ID
;

-- 7
